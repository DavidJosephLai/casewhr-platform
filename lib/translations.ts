export type Language = 'en' | 'zh' | 'zh-TW' | 'zh-CN';

// Helper function to check if language is Chinese (any variant)
export function isChinese(lang: Language | string): boolean {
  return lang === 'zh' || lang === 'zh-TW' || lang === 'zh-CN';
}

// Helper to get translation for a language (with fallback)
export function getTranslation(lang: Language) {
  if (lang === 'zh' || lang === 'zh-TW') {
    return (translations as any)['zh-TW'] || translations.zh;
  }
  if (lang === 'zh-CN') {
    return (translations as any)['zh-CN'];
  }
  return translations.en;
}